#include <iostream>

using namespace std;

int main(){

    int sumatoria=0;
    
    for(int i=2;i<=100;i+=2){
        sumatoria=sumatoria+i;
    }

    cout<<"Resultado de la suma de los numeros pares de 2 a 100: "<<endl;
    cout<<sumatoria;

    return 0;
}
